print("Dias de la semanas")
semana = int(input("Ingresa un dia de la semana del 1 al 7: "))

if semana == 1:
    print("El dia es Lunes")
elif semana == 2:
    print("El dia es Martes")
elif semana == 3:
    print("El dia es Miercoles")
elif semana == 4:
    print("El dia es Jueves")
elif semana == 5:
    print("El dia es Viernes")
elif semana == 6:
    print("El dia es Sabado")
elif semana == 7:
    print("El dia es Domingo")
else:
    print("Ingresa un numero adecuado")